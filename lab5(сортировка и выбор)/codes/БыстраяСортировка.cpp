#include <iostream>
using namespace std;

// Функция для обмена двух элементов массива
void swap(int& a, int& b) {
    int temp = a;  // Храним первое значение во временной переменной
    a = b;         // Присваиваем первому элементу второе значение
    b = temp;      // Второе значение теперь старое значение первого элемента
}

// Процедура разделения массива на левую и правую части
int partition(int arr[], int low, int high) {
    int pivot = arr[high];  // Выбор опорного элемента (обычно крайний правый элемент)
    int i = low - 1;        // Инициализируем указатель левого раздела

    // Проходим по массиву от low до high-1
    for (int j = low; j <= high - 1; j++) {
        // Если текущий элемент меньше или равен опорному элементу
        if (arr[j] <= pivot) {
            i++;           // Перемещаем указатель левого раздела
            swap(arr[i], arr[j]);  // Меняем местами текущий элемент с элементом в позиции i
        }
    }

    // В конце меняем местами опорный элемент с последним элементом левой части
    swap(arr[i + 1], arr[high]);
    return i + 1;  // Возвращаем позицию опорного элемента
}

// Основная рекурсивная функция быстрой сортировки
void quickSort(int arr[], int low, int high) {
    if (low < high) {
        // Определяем точку разделения массива
        int pi = partition(arr, low, high);

        // Рекурсивно сортируем левую и правую части массива
        quickSort(arr, low, pi - 1);  // Левая часть
        quickSort(arr, pi + 1, high); // Правая часть
    }
}

// Вспомогательная функция для вывода массива
void printArray(int arr[], int size) {
    for (int i = 0; i < size; i++)
        cout << arr[i] << " ";
    cout << endl;
}

// Основная функция программы
int main() {
    int arr[] = {10, 7, 8, 9, 1, 5};  // Исходный массив
    int n = sizeof(arr) / sizeof(arr[0]);  // Вычисляем размер массива

    cout << "Исходный массив: ";
    printArray(arr, n);  // Выводим исходный массив

    quickSort(arr, 0, n - 1);  // Вызываем функцию быстрой сортировки

    cout << "Отсортированный массив: ";
    printArray(arr, n);  // Выводим отсортированный массив

    return 0;
}

