#include <iostream>
using namespace std;

// Интерполирующий поиск в отсортированном массиве
int interpolationSearch(int arr[], int n, int target) {
    int low = 0;                       // Нижняя граница поиска
    int high = n - 1;                  // Верхняя граница поиска

    // Пока нижняя граница меньше верхней и целевое значение находится в пределах диапазона
    while ((low <= high) && (target >= arr[low]) && (target <= arr[high])) {
        // Если верхняя и нижняя границы совпадают, проверяем элемент
        if (low == high) {
            if (arr[low] == target)
                return low;             // Возврат индекса найденного элемента
            return -1;                 // Элемент не найден
        }

        // Расчет приблизительного положения целевого элемента
        int pos = low + (((double)(high - low) /
            (arr[high] - arr[low])) *
            (target - arr[low]));

        // Если элемент найден на рассчитанной позиции
        if (arr[pos] == target)
            return pos;                // Возврат индекса найденного элемента

        // Если элемент меньше, двигаемся влево
        if (arr[pos] < target)
            low = pos + 1;             // Установка нижней границы
        else
            high = pos - 1;            // Установка верхней границы
    }

    // Если элемент не найден
    return -1;
}

// Главная функция программы
int main() {
    // Пример отсортированного массива
    int arr[] = {10, 12, 13, 16, 18, 19, 20, 21, 22, 23, 24, 33, 35, 42, 47};
    int n = sizeof(arr) / sizeof(arr[0]);  // Вычисляем размер массива
    int x = 18;                            // Цель поиска

    // Вызываем функцию интерполирующего поиска
    int index = interpolationSearch(arr, n, x);

    // Выводим результат
    if (index != -1)
        cout << "Элемент найден на позиции " << index << endl;
    else
        cout << "Элемент не найден в массиве." << endl;

    return 0;
}

