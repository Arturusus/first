def bubble_sort(arr):
    # Получаем длину массива
    n = len(arr)
    
    # Основной цикл, который выполняется n-1 раз
    for i in range(n - 1):
        # Флаг для отслеживания, произошли ли обмены на текущем проходе
        swapped = False
        
        # Внутренний цикл проходит по массиву, уменьшаясь на единицу на каждом проходе
        for j in range(n - i - 1):
            # Если текущий элемент больше следующего, меняем их местами
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True  # Увеличиваем флаг, так как произошел обмен
                
        # Если на данном проходе не было выполнено ни одной операции обмена,
        # значит массив уже отсортирован, и можно выйти досрочно
        if not swapped:
            break

# Пример использования
if __name__ == "__main__":
    # Тестовый массив
    test_array = [64, 34, 25, 12, 22, 11, 90]
    
    # Печать исходного массива
    print("Исходный массив:", test_array)
    
    # Вызов функции сортировки пузырьком
    bubble_sort(test_array)
    
    # Печать отсортированного массива
    print("Отсортированный массив:", test_array)


